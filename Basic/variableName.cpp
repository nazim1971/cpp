#include <iostream>
using namespace std;

int main ()
{

int a = 5;               // Integer (whole number without decimals)
double b = 5.99;    // Floating point number (with decimals)
char c = 'D';         // Character
string d = "Hello";     // String (text)
bool e = true;       // Boolean (true or false)


cout << "a = " << a << "\n";
cout << "b = " << b << "\n";
cout << "c = " << c << "\n";
cout << "d = " << d << "\n";
cout << "e = " << e << "\n";
return 0;

}
